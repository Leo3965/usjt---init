package ExerciciosRelacionamentoEntreClasses;

class Telefone {
	private String tipo;
	private String ddd;
	private String numero;
	
	String getTipo() {
		return tipo;
	}
	void setTipo(String tipo) {
		this.tipo = tipo;
	}
	String getDdd() {
		return ddd;
	}
	void setDdd(String ddd) {
		this.ddd = ddd;
	}
	String getNumero() {
		return numero;
	}
	void setNumero(String numero) {
		this.numero = numero;
	}

}
